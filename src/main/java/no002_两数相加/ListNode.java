package no002_两数相加;

/**
 * @author mr.24
 * @version Id: ListNode, v 1.0 2021/9/2 12:10 上午 Exp $$
 */
public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

}
